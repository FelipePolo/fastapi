    Row(Modifier.fillMaxWidth(), horizontalArrangement = Arrangement.Center) {
        Text(
            text = "Don't have an account?", fontSize = 12.sp, color = Color(0xFFB5B5B5)
        )
        Text(
            text = "Sign up.",
            Modifier.padding(horizontal = 8.dp),
            fontSize = 12.sp,
            fontWeight = FontWeight.Bold,
            color = Color(0xFF4EA8E9),
        )
    }
}

@Composable
fun Body(modifier: Modifier, bestRouteViewModel: BestRouteViewModel) {
    val email: String by bestRouteViewModel.email.observeAsState(initial = "")
    val password: String by bestRouteViewModel.password.observeAsState(initial = "")
    val result: Car by bestRouteViewModel.car.observeAsState(Car("", "", ""))

    Column(modifier = modifier) {
        ImageLogo(Modifier.align(Alignment.CenterHorizontally))
        Spacer(modifier = Modifier.size(16.dp))
        Email(email) {
            bestRouteViewModel.paramsChanged(email = it, password = password)
        }
        Spacer(modifier = Modifier.size(4.dp))
        Password(password) {
            bestRouteViewModel.paramsChanged(email = email, password = it)
        }
        //Spacer(modifier = Modifier.size(8.dp))
        //ForgotPassword(Modifier.align(Alignment.End))
        Spacer(modifier = Modifier.size(16.dp))
        LoginButton(true) {
            bestRouteViewModel.getBestRoute()
        }
        if (result.route.isNotBlank()) {
            Toast.makeText(LocalContext.current,"Best route: " + result.route, Toast.LENGTH_LONG).show()
        }
    }
}

